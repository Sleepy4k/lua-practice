--[[
  In lua we can use the following data types:
  - nil
  - boolean
  - number
  - string
]]
print(nil)
print(true)
print(false)
print("Hello World!")
print(1)
print(1.0)

--[[
  Also we can check the data type of a variable
  using the type() function
]]
print(type(nil))
print(type(true))
print(type(false))
print(type("Hello World!"))
print(type(1))
print(type(1.0))