--[[
  To use operator in lua, we can use the following operators:
  - Arithmetic Operators
  - Relational Operators
  - Logical Operators
  - Bitwise Operators
  - Other Operators
]]
print(1 + 1) -- Addition
print(1 - 1) -- Subtraction
print(1 * 1) -- Multiplication
print(1 / 1) -- Division
print(1 % 1) -- Modulus
print(1 ^ 1) -- Exponentiation
print(1 .. 1) -- Concatenation
print(1 == 1) -- Equal
print(1 ~= 1) -- Not Equal
print(1 > 1) -- Greater Than
print(1 < 1) -- Less Than
print(1 >= 1) -- Greater Than or Equal
print(1 <= 1) -- Less Than or Equal
print(true and true) -- And
print(true or true) -- Or
print(not true) -- Not
print(1 & 1) -- Bitwise And
print(1 | 1) -- Bitwise Or
print(1 ~ 1) -- Bitwise Xor
print(~1) -- Bitwise Not
print(1 << 1) -- Bitwise Left Shift
print(1 >> 1) -- Bitwise Right Shift
print(1 and 1) -- Other
print(1 or 1) -- Other
print(1 .. 1) -- Other
print(#"Hello World!") -- Other